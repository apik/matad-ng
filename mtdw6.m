miw6 =
    {
        (* Six line *)
        miD6 -> 1/(1-ep)/(1-2*ep)*( 2*Zeta[3]/ep + D6bar[0] + ep*D6bar[1] + ep^2*D6bar[2] + ep^3*Oep[3,D6]),
        miD5 -> 1/(1-ep)/(1-2*ep)*( 2*Zeta[3]/ep + D5bar[0] + ep*D5bar[1] + ep^2*D5bar[2] + ep^3*Oep[3,D5]),
        miD4 -> 1/(1-ep)/(1-2*ep)*( 2*Zeta[3]/ep + D4bar[0] + ep*D4bar[1] + ep^2*D4bar[2] + ep^3*Oep[3,D4]),
        miD3 -> 1/(1-ep)/(1-2*ep)*( 2*Zeta[3]/ep + D3bar[0] + ep*D3bar[1] + ep^2*D3bar[2] + ep^3*Oep[3,D3]),
        miDM -> 1/(1-ep)/(1-2*ep)*( 2*Zeta[3]/ep + DMbar[0] + ep*DMbar[1] + ep^2*DMbar[2] + ep^3*Oep[3,DM]),
        miDN -> 1/(1-ep)/(1-2*ep)*( 2*Zeta[3]/ep + DNbar[0] + ep*DNbar[1] + ep^2*DNbar[2] + ep^3*Oep[3,DN]),

        (* five-line *)
        miE3 -> 1/(-1 + ep)/(1 - 2*ep)^2*(( E3bar[-1]/ep + E3bar[0] + ep*E3bar[1] + ep^2*E3bar[2]+ ep^3*E3bar[3] + ep^4*Oep[4,E3])/Sqrt[3]
                                          + ((Exp[3 EulerGamma ep] Gamma[1 - ep] Gamma[1 + 2 ep] Gamma[1 + ep]^2)/(2 ep^3))*
                                          (1 + (1-ep)/(3*(1-3*ep))*(Gamma[1 + 2 ep] Gamma[1 + 3 ep])/( Gamma[1 + ep] Gamma[1 + 4*ep]))),

        (* four-line *)

        miBN -> ( Exp[3 EulerGamma ep] Gamma[1 + ep]^3/((1 - 3 ep)(2 - 3 ep) (1 - 2 ep) (1 - ep))*     
                  (4/ep^3 - 44/3/ep^2 + 0/ep + 0 + ep*BNbar[1] + ep^2*BNbar[2] + ep^3*BNbar[3] + ep^4*BNbar[4] + ep^5*Oep[5,BN])),
        
        miBN1 -> 9/2*(BN1bar[0] + ep*BN1bar[1] + ep^2*BN1bar[2] + ep^3*BN1bar[3] + ep^4*BN1bar[4] + ep^5*Oep[5,BN1])/Sqrt[3]/(1 - ep)/(-1 + 2*ep)/(-2 + 3*ep)/(-1 + 3*ep) 
        - Exp[3*ep*EulerGamma]*(-1 + ep)^2*(-4 + 15*ep)*Gamma[-1 + ep]^3/2/(-1 + 2*ep)/(-2 + 3*ep)/(-1 + 3*ep),


        (* Two-loop *)

        miT111 -> Exp[2*EulerGamma*ep]*Gamma[1+ep]^2/(-1+ep)/(1-2*ep)/Sqrt[3]*( 
            3*Sqrt[3]/2/ep^2 + T111bar[0] + ep*T111bar[1] + ep^2*T111bar[2] + ep^3*T111bar[3] + ep^4*T111bar[4] + ep^5*Oep[5,T1]),

        (* All orders *)
        Gam[x_, y_]  -> Exp[ep*y*EulerGamma]*Gamma[x + ep*y],
        iGam[x_, y_] -> Exp[-ep*y*EulerGamma]/Gamma[x + ep*y] 

    };





