(* 
   Definitions:
   
   1) HI[w___, om1] -> Im[HPL[{w},Exp[2*I*Pi/3]]]
      HR[w___, om1] -> Re[HPL[{w},Exp[2*I*Pi/3]]]
   
   2) All propagators are eucledian

   3) For each loop integration we divide by Exp[EulerGamma ep]

   4) For HI[___,om1] and HR[___,om1] numerical evaluation with 20k 
   digits accuracy use files nhplIm.m and nhplRe.m
   
   5) Oep[n,TOPO] - truncation function in epsilon expansion at order ep^n for 
   topoogy TOPO

   *)


(* *******************************************************************
